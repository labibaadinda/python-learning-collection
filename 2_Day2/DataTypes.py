# Integer = Whole number
print(123 + 345)

# Large number
# angka yang besar itu bisa dipisah dengan underscore _
# untuk mempermudah visualisasi human readable
# _ tsb tidak akan dibaca oleh komputer
print(123_456_789)

# string
# angka nya langsung digabung gitu
print("123" + "345")

# Float = Floating point number
# koma nya itu titik .
print(3.14567)

# Boolean
print(True)
print(False)